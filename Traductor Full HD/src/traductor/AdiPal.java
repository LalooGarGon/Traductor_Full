    public void cargar() {
        l.add(jLabel2);
        l.add(jLabel3);
        l.add(jLabel4);
        l.add(jLabel5);
        l.add(jLabel6);
        l.add(jLabel7);
        l.add(jLabel8);
        l.add(jLabel9);
        l.add(jLabel10);

        ArrayList<String> pal = new ArrayList();
        for (Palabras aux : Aprender.regist.traer_Palabra()) {
            if (aux.getNivel() == Ejercicios.niv || aux.getNivel() == (Ejercicios.niv - 1)) {
                pal.add(aux.getEnglish());
            }
        }
//      
        int v[] = new int[10];
        int cv = 0;
        System.out.println("Tama√±o arreglo: " + pal.size());
        while (cv < 10) {
            boolean val = true;
            int ran = (int) (Math.random() * ((pal.size() - 1) - 0 + 1) + 0);
            for (int i = 0; i < 10; i++) {
                if (ran == v[i]) {
                    val = false;
                }
            }
            if (val == true) {
                v[cv] = ran;
                v2[cv] = pal.get(ran);
                cv++;
            }

        }

        for (int i = 0; i < 9; i++) {
            ImageIcon icon = new ImageIcon(getClass().getResource("Archivos/" + v2[i] + ".jpg"));
            Icon icono = new ImageIcon(icon.getImage().getScaledInstance(l.get(i).getWidth(), l.get(i).getHeight(), Image.SCALE_DEFAULT));
            l.get(i).setIcon(icono);
        }
    }

