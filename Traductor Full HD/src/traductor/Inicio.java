    private void btnPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrintActionPerformed
        // TODO add your handling code here:
        ArrayList<IPersona> u = Login.registros.consultarUsuario();
        String us = "";
        int uu = 1;
        for (IPersona aux : u) {
            us = us + "User No." + Integer.toString(uu)
                    + "\nName: " + aux.getNombre()
                    + "\nLast Name: " + aux.getApellido()
                    + "\nAge: " + Integer.toString(aux.getEdad())
                    + "\nUser: " + aux.getUsuario()
                    + "\nPassword: " + aux.getPassword()
                    + "\nE-mail: " + aux.getCorreo()
                    + "\nLevel: " + Integer.toString(aux.getNivel())
                    + "\nImage: " + aux.getFoto() + "\n\n";
            uu++;
        }
        Codigo yuki = new Codigo(us);
        yuki.mandarPdf();
        
        int op = JOptionPane.showConfirmDialog(null, "Do you want to open the file?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (op == JOptionPane.YES_OPTION) {
            abrir("Users.pdf");
        }
    }//GEN-LAST:event_btnPrintActionPerformed

