    public void abrir(String archivo) {
        try {
            File objetofile = new File(archivo);
            Desktop.getDesktop().open(objetofile);
        } catch (IOException ex) {
            System.out.println(ex);
        }
    }

    public void cargar() {

        lUsuario.setText(Login.jugador.getUsuario());
        lNivel.setText("Level: " + Integer.toString(Login.nivJug));
        try {
            ImageIcon icon = new ImageIcon(Login.ini + "\\Fotos\\" + Login.jugador.getFoto());
            Icon icono = new ImageIcon(icon.getImage().getScaledInstance(lIma.getWidth(), lIma.getHeight(), Image.SCALE_DEFAULT));
            lIma.setText(null);
            lIma.setIcon(icono);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error al abrir..." + ex);
        }

        if (Login.jugador.getUsuario().compareTo("Lalouw") == 0
                || Login.jugador.getUsuario().compareTo("Pao") == 0
                || Login.jugador.getUsuario().compareTo("Alexis") == 0) {
            btnMarci.setVisible(true);
            btnPrint.setVerifyInputWhenFocusTarget(true);
        } else {
            btnMarci.setVisible(false);
            btnPrint.setVisible(false);
        }
    }

